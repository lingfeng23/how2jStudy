package com.malf.pojo;

/**
 * @author malf
 * @description 指数类，用于指数里的名称和代码
 * @project trendInvestment
 * @since 2020/11/5
 */
public class Index {
	// 指数名称
	String name;
	// 指数代码
	String code;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

}
